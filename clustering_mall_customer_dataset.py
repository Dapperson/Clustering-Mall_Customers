import pandas as pd
from sklearn.preprocessing import MinMaxScaler
data = pd.read_csv('Mall_Customers.csv')
data.head()

import numpy as np
import matplotlib.pyplot as plt
from sklearn.cluster import KMeans
from sklearn.preprocessing import MinMaxScaler

data_clus = data.iloc[:,3:5]
data_clus.head()

plt.scatter(data_clus.iloc[:,0],data_clus.iloc[:,1],c='black')
plt.xlabel('Annual Income')
plt.ylabel('Spending Score')
plt.show()

clus_array = np.array(data_clus)
print(clus_array)

annual_income = data.iloc[:,3]
max_annual_income = max(annual_income)
min_annual_income = min(annual_income)
print("Nilai Maksimal Annual Income")
print(max_annual_income)
print("Nilai Minimal Annual Income")
print(min_annual_income)

spending_score = data.iloc[:,4]
max_spending_score = max(spending_score)
min_spending_score = min(spending_score)
print("Nilai Maksimal Spending Score")
print(max_spending_score)
print("Nilai Minimal Spending Score")
print(min_spending_score)

scaler = MinMaxScaler()
data_scaled = scaler.fit_transform(clus_array)
data_scaled

distortions = []
K = range (1,10)
for k in K:
  kmeanModel = KMeans(n_clusters=k)
  kmeanModel.fit(data_scaled)
  distortions.append(kmeanModel.inertia_)

plt.figure(figsize=(10,6))
plt.plot(K, distortions, 'bx-')
plt.xlabel('k')
plt.ylabel('Distortion')
plt.title('The Elbow Method showing the optimal k')
plt.show()

kmeanModel = KMeans(n_clusters=5)
kmeanModel.fit(data_scaled)
# KMeans(algorithm='auto', copy_x=True, init='k-means++', max_iter=300,
#        n_clusters=5, n_init=10,n_jobs=None,precompute_distances='auto',
#        random_state=None, tol=0.0001, verbose=0)
print(kmeanModel.cluster_centers_)

plt.scatter(data_scaled[:,0], data_scaled[:,1], s=50, c='black', marker='o', alpha=1)
centers = kmeanModel.cluster_centers_
plt.scatter(centers[:,0], centers[:,1], c='red', s=50, alpha=1, marker='o');
plt.title('K-Means Centroid')
plt.xlabel('Annual Income')
plt.ylabel('Spending Score')
plt.show()

y_kmeans = kmeanModel.predict(data_scaled)

plt.scatter(data_scaled[:,0], data_scaled[:,1], c=y_kmeans, s=50, cmap='viridis')

centers = kmeanModel.cluster_centers_
plt.scatter(centers[:, 0], centers[:,1], c='red', s=200, alpha=0.5);
plt.xlabel('Annual Income')
plt.ylabel('Spending Score')
plt.show()

result = pd.DataFrame(data = y_kmeans)
result.columns = ['cluster']
result.head()

output = pd.concat([data,result], axis=1)
output.head()

import seaborn as sns
sns.scatterplot(x=output.iloc[:,3], y=output.iloc[:,4], hue=output.iloc[:,5],
                data=output, palette='viridis', s=40, legend='full');
plt.legend(loc='center right', fontsize='10');

output.to_csv('data_output.csv', index=False)